public class Jugador {

    // ATRIBUTOS

    boolean blanca = true;

    // CONSTRUCTORES

    public Jugador(boolean blanca) {
        this.blanca = blanca;
    }

    // METODOS

    public boolean isBlanca() {
        return blanca;
    }

    public void setBlanca(boolean blanca) {
        this.blanca = blanca;
    }

}
