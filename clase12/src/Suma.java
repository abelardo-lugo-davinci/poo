public class Suma {

    public int sumarDosNumeros(int primerNumero, int segundoNumero) {
        int suma = primerNumero + segundoNumero;
        return suma;
    }

    public double sumarDosNumeros(double primerNumero, double segundoNumero) {
        double suma = primerNumero + segundoNumero;
        return suma;
    }

}
